public class OperacionesAritmeticas {
    public static void main(String[] args) {
        int numeroA = 89;
        int numeroB = 323;

        System.out.println("El valor de numeroA es: " + numeroA);
        System.out.println("El valor de numeroB es: " + numeroB);

        int suma = numeroA + numeroB;
        int resta = numeroA - numeroB;
        int division = numeroB / numeroA;
        int multiplicacion = numeroA * numeroB;

        System.out.println("La suma de numeroA y numeroB es: " + suma);
        System.out.println("La resta de numeroA y numeroB es: " + resta);
        System.out.println("La division de numeroB entre numeroA es: " + division);
        System.out.println("La multiplicacion de numeroA y numeroB es: " + multiplicacion);
    }
}
